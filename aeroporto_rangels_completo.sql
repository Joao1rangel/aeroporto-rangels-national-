-- Ver todos os passageiros de um voo
SELECT v.numero_voo, v.destino, v.data_hora_partida,
       p.nome, p.cpf, r.numero_assento, r.classe
FROM Reserva r
JOIN Passageiro p ON r.id_passageiro = p.id_passageiro
JOIN Voo v        ON r.numero_voo    = v.numero_voo
WHERE v.numero_voo = 1
ORDER BY r.numero_assento;

-- Ocupacao dos voos
SELECT v.numero_voo, v.destino, ca.nome AS companhia,
       ae.modelo AS aeronave, ae.capacidade_passag AS capacidade,
       COUNT(r.id_reserva) AS total_reservas,
       ae.capacidade_passag - COUNT(r.id_reserva) AS assentos_livres
FROM Voo v
JOIN Companhia_aerea ca ON v.id_companhia = ca.id_companhia
JOIN Aeronave ae        ON v.id_aeronave  = ae.id_aeronave
LEFT JOIN Reserva r     ON v.numero_voo   = r.numero_voo
GROUP BY v.numero_voo, v.destino, ca.nome, ae.modelo, ae.capacidade_passag
ORDER BY total_reservas DESC;

-- Passageiros sem reserva hoje
SELECT p.id_passageiro, p.nome, p.cpf, p.email
FROM Passageiro p
WHERE p.id_passageiro NOT IN (SELECT id_passageiro FROM Reserva);
