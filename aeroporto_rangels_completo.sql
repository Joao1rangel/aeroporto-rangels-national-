CREATE DATABASE IF NOT EXISTS aeroporto_rangels
    CHARACTER SET utf8mb4
    COLLATE utf8mb4_unicode_ci;

USE aeroporto_rangels;

-- ============================================================
-- CRIACAO DAS TABELAS
-- ============================================================

CREATE TABLE Companhia_aerea (
    id_companhia    INT             NOT NULL AUTO_INCREMENT,
    nome            VARCHAR(100)    NOT NULL,
    cnpj            CHAR(18)        NOT NULL,
    telefone        VARCHAR(20)     NOT NULL,
    CONSTRAINT PK_Companhia_aerea PRIMARY KEY (id_companhia),
    CONSTRAINT UQ_Companhia_cnpj  UNIQUE (cnpj)
);

CREATE TABLE Aeronave (
    id_aeronave       INT          NOT NULL AUTO_INCREMENT,
    modelo            VARCHAR(100) NOT NULL,
    fabricante        VARCHAR(100) NOT NULL,
    capacidade_passag INT          NOT NULL,
    id_companhia      INT          NOT NULL,
    CONSTRAINT PK_Aeronave           PRIMARY KEY (id_aeronave),
    CONSTRAINT FK_Aeronave_Companhia FOREIGN KEY (id_companhia)
        REFERENCES Companhia_aerea(id_companhia)
);

CREATE TABLE Portao (
    numero_portao INT         NOT NULL AUTO_INCREMENT,
    status        VARCHAR(20) NOT NULL DEFAULT 'Disponivel',
    CONSTRAINT PK_Portao PRIMARY KEY (numero_portao)
);

CREATE TABLE Passageiro (
    id_passageiro INT          NOT NULL AUTO_INCREMENT,
    cpf           CHAR(14)     NOT NULL,
    nome          VARCHAR(150) NOT NULL,
    telefone      VARCHAR(20)  NOT NULL,
    email         VARCHAR(150) NOT NULL,
    CONSTRAINT PK_Passageiro     PRIMARY KEY (id_passageiro),
    CONSTRAINT UQ_Passageiro_cpf UNIQUE (cpf)
);

CREATE TABLE Voo (
    numero_voo        INT          NOT NULL AUTO_INCREMENT,
    origem            VARCHAR(100) NOT NULL,
    destino           VARCHAR(100) NOT NULL,
    data_hora_partida DATETIME     NOT NULL,
    status            VARCHAR(20)  NOT NULL DEFAULT 'Programado',
    id_aeronave       INT          NOT NULL,
    id_companhia      INT          NOT NULL,
    numero_portao     INT          NOT NULL,
    CONSTRAINT PK_Voo           PRIMARY KEY (numero_voo),
    CONSTRAINT FK_Voo_Aeronave  FOREIGN KEY (id_aeronave)
        REFERENCES Aeronave(id_aeronave),
    CONSTRAINT FK_Voo_Companhia FOREIGN KEY (id_companhia)
        REFERENCES Companhia_aerea(id_companhia),
    CONSTRAINT FK_Voo_Portao    FOREIGN KEY (numero_portao)
        REFERENCES Portao(numero_portao)
);

CREATE TABLE Reserva (
    id_reserva     INT         NOT NULL AUTO_INCREMENT,
    numero_assento INT         NOT NULL,
    classe         VARCHAR(30) NOT NULL,
    data_emissao   DATE        NOT NULL,
    id_passageiro  INT         NOT NULL,
    numero_voo     INT         NOT NULL,
    CONSTRAINT PK_Reserva            PRIMARY KEY (id_reserva),
    CONSTRAINT FK_Reserva_Passageiro FOREIGN KEY (id_passageiro)
        REFERENCES Passageiro(id_passageiro),
    CONSTRAINT FK_Reserva_Voo        FOREIGN KEY (numero_voo)
        REFERENCES Voo(numero_voo)
);

